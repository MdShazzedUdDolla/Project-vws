            <div data-bs-toggle="collapse" href="#faq3" class="collapsed question">WHY HAVE I BEEN ASKED TO PARTICIPATE IN THE RESEARCH PROJECT? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
            <div id="faq3" class="collapse" data-bs-parent=".faq-list">
              <p>
              You have been invited to participate in this research project because you fit the eligibility criteria for the study.  This means that you are currently a University of Prince Edward Island undergraduate student, lasting the duration of the study.<br>
Individuals that are interested in participating in the Connections program will be invited to attend an information session to be held both virtually and in-person at UPEI. Health Centered Research Clinic staff will explain the program and the various activities in which participants will be involved, along with expected time commitments. Interested participants will be required to read through the background information form and sign the consent form once completed the participant will be asked to do the Physical Activity Readiness Questionnaire (PAR-Q) as part of the program screening procedure to determine if the participant is medically eligible to participate without the need of medical clearance. If the participant requires medical clearance they will be asked to do so, prior to intake of the program. If interested to continue in the Connections program, individuals may schedule a visit to the Health Centered Research Clinic whereupon they will complete a series of intake assessment forms. The intake forms will be administered by a Health Educator and Dietitian before the participant begins the program. After the participants will take part in a series of physical assessments administered by a kinesiologist that include measures of height, weight, resting heart rate, blood pressure, back scratch test, grip strength and plank test.

              </p>
