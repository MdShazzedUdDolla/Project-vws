          <li>
            <div data-bs-toggle="collapse" href="#faq4" class="collapsed question">WHAT WILL MY RESPONSIBILITIES BE IF I CHOOSE TO PARTICIPATE IN THIS RESEARCH PROJECT? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
            <div id="faq4" class="collapse" data-bs-parent=".faq-list">
              <p>
              If you choose to participate in this research project you will first be asked to sign the letter of informed consent. Once the consent has been signed the next form you will need to fill out is the PAR-Q form which assesses your physical readiness. If the PAR-Q provides us with a score that shows that you are unable to participate in the physical component you will be asked to receive clearance from a healthcare professional before we can proceed with the next component of the study. Once the consent form and PAR-Q have been cleared. You will also be asked to complete an initial intake day.  The initial intake day will be held during the day and will be scheduled for 1.5 hours.  At this time, you will complete a series of non-invasive physical tests.  The physical tests will include: balance testing, one mile walk, half kneeling ankle flexion, back stratch test, resting heart rate, height/weight measurement and endurance of extensors. In addition to the physical tests, you will be asked to complete a number of surveys.  If you run out of time completing the surveys and questionnaires, you may take them with you and return the completed copies at your first session. Multiple surveys will be provided and they will be asking questions about: perceived stress, loneliness, willingness to change, physical activity readiness questionnaire, confidence questionnaire where questions of sensitive nature are asked; “How confident are you regarding practicing safe guidelines for consuming alcohol?” “How confident are you regarding your knowledge when it comes to practicing safe sex?”. All testing and sessions will take place at the Health Centred Research Clinic, located in the lower level of the Steel Building on the UPEI campus.
              <br>Once you have completed your initial intake day, you may attend your first session.  Your first session will be either an education or exercise session, depending on what group you have been allocated to.  Both groups will receive all of the education and exercise sessions.  At the education sessions, you will meet with the Health Centred Research Clinic’s Health Educator.  She will discuss various topics related to health, well-being and skills development. At the exercise sessions, you will meet with the Health Centred Research Clinic’s Kinesiologist.  He will demonstrate and work with you to complete a guided group exercise session.  He will also provide you with a tailored-to-you exercise routine to complete at home. All sessions will be 45 minutes in length, and will be held during various times of the day. You will be asked to complete activities and participate in group discussions throughout the education and exercise sessions. These sessions will be held once a week for 8 weeks.  This means you will be offered 8 education sessions and 8 exercise sessions.
	<br>Upon completion of the program, you will be asked to come to the Health Centred Research Clinic to complete all of the tests from the initial intake day.<br>
	You may also be asked to attend a focus group meeting to give feedback on your thoughts about the program, strengths of the program and areas of improvement.  These focus groups will give the research team a better understanding of areas of the program that should be changed or kept the same, should they continue the delivery of the program to different groups of people in the future.
	<br>If you choose to participate in the research study, your total time commitment will be the following:
<br>Initial intake day: 1.5 hours
<br>8 Education sessions: 8 sessions x 1 hour= 8 hours
<br>Post program assessment: 1.5 hours
<br>Total time commitment for participation in the research study: up to 19 hours

              </p>
